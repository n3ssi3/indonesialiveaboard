                            <p align="left" class="style19">
                              The use of  the noun &ldquo;boutique&rdquo; as an adjective describing hotels that cater to a small  niche and offer a more personal service has become commonplace recently in  Bali, following a trend that began a few years ago in cities in Europe and the  USA.<br>
                              However, in  one respect Bali is leading the boutique trend and that is in the world of  floating hotels for scuba divers known as live a boards.<br>
                              <br>
                              <strong><em>Liveaboards</em></strong><br>
                              These are  boats that offer full-board accommodation and diving services, giving their  guests the opportunity to dive in remote areas far away from major population  centers, places where the marine life is more abundant and the environment  comparatively pristine.<br>
                              <br>
                              Liveaboards have flourished in recent years in response to demand from divers  with busy lives who want to make the most of their limited leisure time.  Because they are living right on the top of the sites they can easily do four  or five dives a day, compared to the 2 dives a commonly offered by land based  operators.<br>
                              Another  reason for their success is that diving from a live a board does not require  the same physical effort as shore or small boat diving and this appeals to the  baby boomers in the West who where the first generation that dived for fun  rather than work and who are now at an age where options that require the  expenditure of less energy tend to be more attractive.<br>
                              <br>
                              <strong><em>The Liveaboard Life</em></strong><br>
                              If you&rsquo;re a  keen diver the live a board experience is as close to perfect as life can get.  Yo wake up right on top of one of the world&rsquo;s best dive sites and start the day  with a small first breakfast before pulling on your wetsuit, slipping into your  dive gear, dropping into the clear, warm ocean above a coral reef teeming with  fish all waiting to wish you a good morning and an eventful day. An hour or so  later you ascend to find the crew waiting to pick you up in a speed boat,  relieve you of your equipment and drive back to the mother ship where a hot  shower and a second breakfast are waiting for you. The pattern of the day is  thus set, eat, rest, dive, eat, rest, dive until you finally sink into slumber.  &nbsp;Serenaded by the sounds of the sea.<br>
                              The  disadvantages are that you&rsquo;re living on a boat, which can be uncomfortable if  sea conditions become rough. Your food options are limited and nightlife is  usually out of reach.<br>
                              If you do not get on with your fellow travelers it is hard to escape but  liveabaords are usually very convivial places full of like-minded, outgoing  folks. If you were a non diver the topics of conversation around the dinner  table would probably send you screaming to your cabin but if, like most divers,  you never tire of discussing whether a photo of a pigmy sea horse shows a  bargibanti or dense or debating the merits of split fins then a live a board  lounge is where you belong.<br>
                              Options exist  for all budgets, from dormitory accommodation in bunks for backpackers to  palatial cabins for the well heeled. The catering choices range from  canteen-style buffets to a la cart fine dining. Some boats offer weekend  schedules out of a busy city, others extended voyages lasting for several weeks  which really let get away from it all. In some places the operations can best  be described as niche market cruise ships.<br>
                              <br>
                              <strong><em>Indonesia is just  perfect&hellip;</em></strong><br>
                              For liveaboard  scuba diving. With its 17000 thousand plus islands located in the middle of the  coral triangle and with the greatest marina life diversity on earth, Indonesia  is home to some of the most fabulous diving in the world and liveaboards are  the perfect way to experience the best of what the archipelago has to offer  underwater.<br>
                              With  increasing demand the number of vessels has grown accordingly and there are now  a few dozen boats operating here, although moves are afoot to limit numbers.  The Komodo strait between Sumbawa and Flores and the islands of Raja Ampat, off  the coast of North-West Papua, are the most frequently visited areas and both  have deservedly attained a reputation as world-class dive destinations.</p>
