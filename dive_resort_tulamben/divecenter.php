                    <P align=center class="style16 20wh"><strong>Dive Center Tulamben - <br>
                      SSI Platinum &amp;
                      Padi 5 Star  Dive Center</strong></P>
                    <P align=justify class="style21">Unlike the most resorts in Bali, the Tauch Terminal Resort Tulamben has to offer three outstanding dive sites directly in front of the adjacent beach. Below you will find comprehensive information about dive sites and the resort's dive center.</P>
                    <P
                        align=left><span class="style21">The dive center features a comfortable diver's hangout for post dive chats and meetings plus a multimedia air-conditioned classroom. Coltri and Max air compressors in combination with a Nitrox filling station provide best air mixtures to fill our 44 aluminium (80 cbf.) tanks. Dacor and Spiro dive sets, as well as torches, cameras and dive computers are for rent. Of course, comfortable transport vans and all safety equipment necessary to run a safe and professional dive service are standard features of our dive resort. We offer all PADI diving courses from beginner to professional. Our big swimming pool has a shallow area and reaches 3 meters at the deepest point, thus is an ideal training location for confined water sessions of each course. The Tauch Terminal Resort Tulamben is a certified PADI 5-Star IDC Center. More information about diving courses can be found on our Tauch Terminal website <A
                        href="http://www.tauch-terminal.com/english"
                        target=_blank class="style30"> http://www.tauch-terminal.com/english</A>.</span><BR>
                        <BR>
                        <img src="resortimages/divecenterpic1.jpg" alt="indonesia_liveaboard_scuba_diving_resort" width="204" height="136" hspace="11" vspace="4" border="1" align="left"><span class="style21">Tulamben Wreck is situated directly near the beach on Bali's northeast coast. The 120m long wreck rests in a 90 degree angle with the deck facing the sea side and used to be an American supply ship named „USA Liberty". Anchored off the coast of Lombok, the ship was hit by a Japanese torpedo during World War II. Miraculously nobody got hurt, but the damage turned the ship into a non-functioning and useless hull. The American Navy's plan to tow the ship to Singaraja harbor (north Bali) failed as the harbor was completely occupied, so the ship was intentionally stranded on the rocky beach of Tulamben, where it was unloaded. In 1963 the volcano Agung erupted and the magma flow pressed the ship back into the sea where it presently rests at a depth of 3 to 29 meters. Since then, coral covered the wreckage completely and turned it into a home for many species of fish. From time to time, a Napoleon fish or huge barracuda drops by. You may dive or snorkel this wreck comfortably, accessing it directly from the beach.<BR>
                        </span><span class="style27"><BR>
                        <span class="style18">Tulamben Wreck at a glance</span></span><BR>
                        <span class="style21">Reef characteristics: wreck <BR>
  Visibility: 15-30 m <BR>
  Current: none to moderate <BR>
  Coral growth: wreck is completely covered by stone- and soft coral<BR>
  Marine life: vast schools of fish (fusiliers and surgeon fish) <BR>
  Highlight: the wreck is easy to dive even suitable for discover scuba diving</span></P>
                    <P
                        align=center><span class="style15"><img src="resortimages/diveresort.jpg" alt="indonesia_liveaboard_scuba_diving_resort" width="449" height="135" hspace="10" vspace="10" border="1"><br>
                        <span class="12wh"><span class="style31"><strong><span class="style23">Please have a look at our dedicated Dive Resort homepage: </span></strong><a href="http://www.tulamben.com"><strong></strong></a></span></span> </span></P>
                    <P
                        align=center><span class="style31"><a href="http://www.tulamben.com"><strong>www.tulamben.com</strong></a></span></P>
                    <P
                        align=justify><strong><span class="style18">Tulamben Wall</span><br>
                      </strong><span class="style21">Divides into three great dive sites: the wall, the coral garden and shark point. All dive sites can be accessed directly from the beach. </span><BR>
                      <BR>
                      <span class="style21">The Wall starts at a depth of 1 m to then drop straight to 40 m. Besides amazing coral growth this reef also features an unbelievable landscape sculpted by many overhanging rocks. The presence of smaller reef fish is abundant, with angel fish being the species worth mentioning. Besides emperor angel fish, yellow-faced angelfish and blue-banded angel fish can be found with the duke fish rounding out the selection as a member of the angel fish family. Quite often, huge schools of jack fish pass the wall, darkening the sun. Although it is a wall, this dive site is also suitable for snorkeling. <BR>
                      <BR>
  The coral garden is only about a hundred meters away from our Tauch Terminal Resort Tulamben, which is located directly at the beach. The coral garden starts at a depth of 2-15 m and is wonderfully grown featuring stone- and soft coral alike. Besides numerous species of reef fish, the beautiful blue-yellow ribbon moray eel has found a home here. This dive site is also a superb snorkelling spot with none to moderate current.</span></P>
                    <P
                        align=center><img src="resortimages/divecenterpic5.jpg" alt="indonesia_liveaboard_scuba_diving_resort" width="292" height="198" border="1"></P>
                    <P
                        align=justify><BR>
                        <BR>
                        <span class="style21">Shark Point can be reached via snorkelling close to the wall. The residing sharks are commonly whitetip- and blacktip reef sharks and may rest at a depth of 20 to 30 m in a sandy area. When currents are present, occasionally grey reef sharks and even hammerhead sharks circle the blue. The current may become quite strong here and sometimes cold water might swell up forming thermoclines. Thermoclines may lower the water temperature to 24 degrees for a short period of time. This dive site is not suitable for snorkelling. </span><BR>
                        <BR>
                        <strong><img src="resortimages/divecenterpic2.jpg" alt="indonesia_liveaboard_scuba_diving_resort" width="204" height="162" hspace="11" border="1" align="left"><span class="style18">Tulamben drop off at a glance</span></strong><BR>
                        <span class="style21">Reef characteristics: drop off <BR>
  Visibility: 15-30 m <BR>
  Current: none to moderate (shark point to strong) <BR>
  Coral growth: many stone and soft coral <BR>
  Marine life: many species of reef fish, specially angelfish <BR>
  Highlight: easy to dive, also visited during the open water course</span></P>
                    <P align=left class="14wh"><strong><BIG>Special Dive sites and Daytrip Info at a glance</BIG></strong></P>
                    <P align=justify class="style21"><strong>Night diving</strong><BR>
  All above mentioned sites are ideal for a night dive. You will see the Bumphead sleeping family in the wreck, morey eels swimming or even the spanish dancer!<BR>
  <BR>
  <strong>Batu Kelebit</strong><BR>
  This site can ben reached by boat (10 minutes ride with the local fisherman's boat). A higher chance of bigger fish. A huge napoleon and whitetip reef sharks are frequently encountered here.</P>
                    <P align=justify> <span class="style21"><strong>Nusa Penida</strong></span><BR>
                        <span class="style21"> The island is known for its exceptional drift dive. Life is just extraordinary, and visibility usually over 30-40 metres. The biggest chance to see big fish.<BR>
                        <strong><img src="resortimages/divecenterpic3.jpg" alt="indonesia_liveaboard_scuba_diving_resort" width="204" height="162" hspace="11" border="1" align="right"></strong></span></P>
                    <P align=justify class="style21"><strong>BBQ / Mimpang</strong><BR>
  We dive the shark point where encounter is most likely, and also at Mimpang where we usually see whitetip reef sharks.<BR>
  <BR>
  <strong>Amed</strong><BR>
  The small village Amed hide various dive sites accessible form the shore with the local fishermen's boats. Nice corall walls and abundant marine life.</P>
