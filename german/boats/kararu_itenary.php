            <h3 class="style32">Gruppen Buchungen </h3>
            <p>A group booking is defined as a minimum of 8 and a  maximum of 17 passengers. Group bookings are charged at the published  individual rate per night but qualify for one or two free of charge spaces  depending on the size of the group.</p>
            <p>Group bookings will be allocated regular cabins  unless the staterooms are requested.  Staterooms and single cabins are charged at the published individual rate per  night. Cabins not booked as part of a group booking will be available for other  divers. </p>
            <h3 class="style32">Kompletter Boot Charter </h3>
            <p>A full boat charter is defined as 18 to 20 passengers,  the charter rate is calculated at the lowest published room rate per person  (excluding free of charge passengers) and includes both upper deck staterooms.</p>
