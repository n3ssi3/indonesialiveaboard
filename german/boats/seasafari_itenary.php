                        <h3>Day One: Arrival Sorong Airport</h3>
                        <p>Kararu dive masters will meet you at the airport and escort   you to the ship for immediate boarding. Cabin allocation and ship   orientation will take place before departure at 3:00 PM. Take this   opportunity to familiarize yourselves with the vessel, set up your   equipment, and make yourself comfortable. Dinner will be served while   sailing towards our first destination.</p>
                        <h3>Day Two: Kri Island</h3>
                        <p>Welcome to Kri Island. The reefs in this area are protected   by Max Ammer from the Papua Diving Resort. If you would like to add   some diving days before the cruise you can just stay at his resort and   join the boat right outside the Lagoon. Kri is a great start for the   cruise with the most fish and coral species accounted on a single   divesite. Truly pristine reefs, you will want to dive these sites around   this island again and again. A night dive will be offered.</p>
                        <h3>Days Three &amp; Four: Waigeo Island Group</h3>
                        <p>Welcome to the incredible Waigeo Island  group. You will be   in awe at the beauty  of yellow Wall boasting a very large amount of   yellow soft coral. In the afternoon a dive on the sheer wall  undercut   at the edge from millions of years of oceanic erosion will truly amaze.   An interesting pearl farm is nearby (the 5th biggest in Asia) and can be   visited upon request. The night dive here is not to be missed!  The   chances to see bigger animals including mantas are high in this area.</p>
                        <h3>Day Five: Kawe Island</h3>
                        <p>Draping directly over the equator lies Kawe Island. Kararu    Dive Voyages will arrive early morning in and anchor right on the   equator. Enjoy breakfast while watching the stunning  scenery. You will   be offered up to five  dives today to take advantage of this world-class   dive region. You will be treated to an enormous array of fish life as   well as stunning above water scenery.</p>
                        <h3>Days Six &amp; Seven: Waiag And Southern Islands</h3>
                        <p>You will move on to our most northerly  area of Irian Jaya   with many unnamed groups of islets. From mild sloping coral gardens with   huge coral rocks and colorful soft corals, to steep walls and schooling   fish, this  region is truly spectacular. Here you can make the famous   &lsquo;hike&rdquo; to view the breath taking scenery over the island group here.   Fantastic night dives with rare wobbygong shark spottings are also on   the agenda.</p>
                        <h3>Days Eight &amp; Nine: Manosuar Island</h3>
                        <p>Arriving early morning in Manosuar, you  can expect to dive   the famous sardine reef as well as 3 other incredible sites. The fish   life is prolific and the topside scenery will truly astound. Two full    days diving will be offered in this amazing region as well as a great   top deck barbeque to satisfy those hungry divers.</p>
                        <h3>Day Ten: Batanta Island</h3>
                        <p>To start your day, you will take a two  hour morning hike   in search of the Red Bird of Paradise. A true jungle trek, you will hear   giant hornbills, cockatoos flying by and perhaps if lucky, the   mesmerizing song of  the bird of Paradise. Today&rsquo;s diving day can host   anything form wobbygong sharks to pigmy seahorses.</p>
                        <h3>Day Eleven: Wai Island</h3>
                        <p>After a light breakfast, a beautiful dive will be offered   at the American plane wreck that crashed in these waters in the 1940&rsquo;s.   The wreck is upside down and between 24 - 34 m (90-115&rsquo;) deep. The wreck   is fairly intact with excellent coral covering it especially on the   lower side of the tail section. The guns are also visible on this truly   great dive. The rest of the day will be spent around the incredible   sites of Wai Island. If desired, you may take a short trip on shore to   see the resident cassowary bird.</p>
                        <h3>Day Twelve: Arrival in Sorong</h3>
                        <p>The ship will arrive at 6:00 AM at Sorong. You will be   served breakfast and afterwards  prepare for disembarkation. You will be   transferred to the airport for your onward flight.</p>
                        <h3>Variations</h3>
                        <p>The Irian Jaya - Raja Ampat liveaboard is usually an eleven   night cruise but in case of twelve night cruises we will spend an extra   night at Wei Island on day twelve and arrive in Sorong on day thirteen.                        </p>
                        <h2 align="center" class="style15"><strong>For the KLM Sea Safari VIII in 2013,  please contact us for rates and availability.</strong></h2>
<h3 class="style32">group bookings</h3>
                        <p>Group bookings are available for any regular scheduled   cruise on either vessel. A group booking is defined as a minimum of 8   and a maximum of 13 passengers. Group bookings are charged at the   published individual rate per night but qualify for one free of charge   space. Any remaining cabins not booked as part of your group booking   will be available for other divers.</p>
                        <p>Group bookings will be allocated regular cabins  unless the staterooms are requested.  Staterooms and single cabins are charged at the published individual rate per  night. Cabins not booked as part of a group booking will be available for other  divers. </p>
                        <h3 class="style32">full boat charters</h3>
