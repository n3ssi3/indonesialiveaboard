                                <TD><p>Komodo ist der wohl bekannteste Nationalpark Indonesiens.<br>
                                  Neben den nur hier lebenden, grossen Komodowaranen,
                                  die bei einem Landausflug beobachtet werden k&ouml;nnen, sind
                                  in diesem Gebiet viele gross- und einzigartige Tauchpl&auml;tze
                                  zu finden.</p>
                                    <p align="center"><a href="Amira/komodo-national-park-dragon.jpg" target="_blank"><img src="Amira/komodo-national-park-dragon.jpg" alt="komodo waran dragon" width="449" height="176" border="0"></a></p>
                                  <p align="left"> In der Regel starten und enden die Touren in Bali.
                                    Schon nach der ersten Nacht wird die Insel Satonda erreicht,
                                    wo auch die ersten Tauchg&auml;nge stattfinden.<br>
                                    In der zweiten Nacht erreicht die &laquo;Amira&raquo; die Vulkaninseln
                                    Sangean. Hier begeistert unter anderem der Black
                                    Forest mit seinem Reichtum an schwarzen Korallen, heissen
                                    Quellen und den aus dem Sand aufsteigenden Gasblasen.<br>
                                    Nach einer kurzen &Uuml;berfahrt wird die Insel Komodo erreicht.<br>
                                    Tauchg&auml;nge an ber&uuml;hmten, fischreichen Pl&auml;tzen wie
                                    Castle Rock und Manta Valley lassen Taucherherzen h&ouml;her
                                    schlagen. Starke Str&ouml;mungen f&uuml;hren zu spektakul&auml;ren Tauchg&auml;ngen
                                    mit grossem Fischreichtum. Riesige Schw&auml;rme,<br>
                                    Haie oder Mantas k&ouml;nnen hier fast garantiert werden.<br>
                                    S&uuml;dlich von Komodo befindet sich die Insel Rinca mit
                                    der Horseshoebay. Pr&auml;chtig bewachsene Steilw&auml;nde und
                                    beeindruckende Makrotauchg&auml;nge machen Rinca zu einem
                                    der zahlreichen Highlights der Tour. Cannibal Rock und
                                    Larrys Yellow Wall of Texas sind auch f&uuml;r sehr anspruchsvolle
                                    Taucher ein Leckerbissen.<br>
                                            <br>
                                  </p></TD>
