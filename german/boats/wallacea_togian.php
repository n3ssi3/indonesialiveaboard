                <BR>
                <B>Fishomania</B> ist der Gipfel eines Unterwasserberges in 12 Fuss Tiefe. Dort tummelt sich eine unglaubliche Vielfalt an Fischen und wir spenden einige Minuten, um uns in der Str<span class="style11">&ouml;</span>mung an die Felsen zu h<span class="style11">&auml;</span>ngen und uns an dem Ballet der tausend bunten Fische zu erg<span class="style11">&ouml;</span>tzen. <BR>
                <BR>
                <B>Manara 1, Una-Una Isl., 45 bis 90 Fu<span class="style11">&szlig;</span></B><BR>
                Am Una-Una Barrier Riff, finden wir an einen mit Steinkorallen bedeckten Hang. Ein weiteres Highlight dieses Tauchgebietes sind die in 90 Fu<span class="style11">&szlig;</span> tiefe gelegenen weissen Gorgonian Felder am Anfang unseres Tauchgangs. Auch dort finden wir eine unglaubliche Vielfalt an Fischen. <BR>
                <BR>
                A<B>pollo Riff, Una-Una Insel, 90 Fu<span class="style11">&szlig;</span></B><BR>
                ...ist eine Rifformation an einem sandigen Hang in 25-35 Meter Tiefe. Auch hier finden sich wieder Schw<span class="style11">&auml;</span>rme von bunten tropischen Fischen ein - vor allem die gro<span class="style11">&szlig;</span>en Barracuda-Schw<span class="style11">&auml;</span>rme sorgen bei unseren Tauchern f<span class="style11">&uuml;</span>r Begeisterung. <BR>
                <BR>
                <B>Kololio, Una-UnaInsel, 30 Fu<span class="style11">&szlig;</span>, Nachttauchgang </B><BR>
                Neben den vielen bizarren und bunten Kleinstlebewesen (Critters) die bei Nacht den Meeresgrund bev<span class="style11">&ouml;</span>lkern, sehen wir auch Schildkr<span class="style11">&ouml;</span>ten, schlafende Papagaienfische, Brassen und viele andere Arten, die  einen Besuch allemal wert sind. <br>
                <BR>
                <B>Mimpi Ding-Ding, Pasir Tengah Atoll, 65 bis 100 Fu<span class="style11">&szlig;</span></B><BR>
                Wir tauchen an einer Steilwand entlang die in absolut senkrecht in unergr<span class="style11">&uuml;</span>ndliche Tiefe f<span class="style11">&auml;</span>llt. Die Tiefe und das transparente Blau des Ozeans geben diesem Tauchplatz ein ganz besonderes Ambiente. Dies ist der &quot;Urtyp&quot; einer Steilwand, bewachsen mit den sch<span class="style11">&ouml;</span>nsten schwarzen Korallen und Schw<span class="style11">&auml;</span>mmen.<BR>
                <BR>
                <B>Goa-Goa, Pasir Tengah Atoll,  55 Fu<span class="style11">&szlig;</span></B><BR>
                Eine weitere Steilwand - ein hervoragender Platz zum fotografieren, sch<span class="style11">&ouml;</span>n bewachsenen mit Weichkorallen. Nach einer etwas l<span class="style11">&auml;</span>ngeren Schwimmstrecke kommen wir an eine Wand in 3-6 Metern Tiefe, die mit einer unglaublichen Anzahl an verschiedenen Spezies von Nacktschnecken (Nudi Branches) bev<span class="style11">&ouml;</span>lkert ist.<BR>
                <BR>
                <B>Batu Gila ("Crazy stone"), 100 Fu<span class="style11">&szlig;</span></B><BR>
                Ein spezielles Gebiet fuer &quot;Pelagics&quot; ausserhalb des Barrier Riffs von Kadidiri. Die h<span class="style11">&ouml;</span>chste Stelle des Riffs liegt bei 27 Meter und f<span class="style11">&auml;</span>llt bis zum sandigen Grund auf 70 Meter ab. Wenn an bestimmten Zeitabschnitten der Gezeiten die Str<span class="style11">&ouml;</span>mungsverh<span class="style11">&auml;</span>ltnisse zunehmen, nimmt die Dichte des Fischlebens ebenfalls immer mehr zu. Dieses Gebiet empfehlen wir nur erfahrenen Tauchern. <BR>
                <BR>
                <B>Gunung laut, South Togians, Tiefe 80 Fu<span class="style11">&szlig;</span></B><BR>
                Dieser Unterwassergipfel kommt aus 200 Metern Tiefe und endet bei <br>
                ca. 27 Metern - diese Bergspitze wird von zahllosen Fischarten bev<span class="style11">&ouml;</span>lkert. Graue und wei<span class="style11">&szlig;</span>e Riffhaie, Thunfische, ect plus wundersch<span class="style11">&ouml;</span>n gewachsenen Weichkorallen, begeistern jeden Taucher. <br>
