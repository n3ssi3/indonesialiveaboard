Nach einem Erfrischungsdrink setzen wir <strong>Segel </strong> in Richtung Osten. <strong>Kurze Einf&uuml;hrung </strong> an Bord sowie <strong>Programmbesprechung </strong>. Danach gibt's nur eines zu tun: entspannen &amp; die gro&szlig;artige Landschaft genie&szlig;en! Wir cruisen entlang der K&uuml;ste Bali's durch die Lombok Stra&szlig;e mit Kurs auf unser erstes Ziel, die Insel <strong>Satonda. </strong></p>
<p><span style="font-weight: bold; font-family: Verdana, Arial, Helvetica, sans-serif;">                          </span><strong>2. Tag: Satonda <br>
</strong>Die kleine <strong>unbewohnte Vulkaninsel Satonda </strong> liegt vor der K&uuml;ste Sumbawa's. “ <strong>Check-Tauchgang” </strong> an einem sanften Abhang mit <strong>Riesenschw&auml;mmen </strong> und <strong>Seefarn </strong>. Anschlie&szlig;end tauchen wir <strong><em> “Lake Entrance Bay” </em></strong> und “ <strong><em>Sand Chute </em></strong>”. Von <strong>Korallenfingern </strong> durchzogene Sandfl&auml;chen mit <strong>R&ouml;hrenaalen </strong>, <strong>Teufelsfischen </strong>, <strong>Schnecken </strong>, blauen <strong>Seesternen </strong> &amp; bunten <strong>Seescheiden </strong>. Landausflug zum <strong>mysteri&ouml;sen Kratersee </strong>. Einheimische kommen hierher um einen Wunsch auszusprechen. Sie binden einen Stein an einen Ast, wenn der Stein einmal ins Wasser f&auml;llt soll der <strong>Wunsch in Erf&uuml;llung </strong>gehen. Nach dem Essen in der Bucht von Satonda geht es weiter nach <strong>Gili Banta </strong>. </p>
<p><span style="font-weight: bold; font-family: Verdana, Arial, Helvetica, sans-serif;">                            </span><strong>3. Tag: Gili Banta <br>
</strong><strong>Gili Banta </strong> ist die erste Insel im Norden des <strong> Komodo Archipels </strong>. Die mondf&ouml;rmige Bucht ist ein idealer Ankerplatz &amp; Ausgangspunkt f&uuml;r Tauchg&auml;nge. “ <strong><em>GPS Point </em></strong>” <strong><em></em></strong>ist <strong><em></em></strong>ein Unterwasser Berg mit toller Sicht, teils starker Str&ouml;mung und vielen <strong>Riffhaien </strong>. Riffdach mit riesigen <strong>Hirnkorallen </strong>, &uuml;ber und &uuml;ber bewachsen. “ <strong><em>Dive Star Wars </em></strong><em>” </em>, riesige Fischschw&auml;rme, sehr gutes Korallenwachstum sowie gelegentlich <strong>Mantas </strong>. “ <strong><em>Dive K2 </em></strong>” <strong><em></em></strong>beherbergt besonders viele <strong>Clownfische </strong>, aber auch winzige, <strong>bizarre Meeresbewohner </strong> wie <strong>Korallenw&auml;chter </strong>, <strong>Blattfische </strong> &amp; <strong>Seenadeln </strong>. Sp&auml;ter am Nachmittag setzen wir in Segel Richtung Westen, zum <strong>Komodo National Park </strong>. </p>
<p><span style="font-weight: bold; font-family: Verdana, Arial, Helvetica, sans-serif;">                              </span><strong>4. Tag: Komodo National Park <br>
</strong>Morgens Ankunft in <strong>Komodo </strong>. Schon w &auml;hrend der Registrierung im Nationalpark ersp &auml;hen wir die ber &uuml;hmten “ <strong>Drachen </strong>”. Die <strong>Komodo Warane </strong> sind die <strong>gr&ouml;&szlig;ten Echsen der Welt </strong>. Zweist&uuml;ndiger “ <strong>Dragon-Walk </strong>”. Zur&uuml;ck auf <strong>Moana </strong> tauchen wir “ <strong><em>Pink Beach </em></strong>”- Fische in allen Farben &amp; Formen: Pfauen Kaiserfische, Lippfische, Doktorfische… <em></em>Strandspaziergang am einzigen rosaroten Strand der Welt. Kurze &Uuml;berfahrt nach Padar wo wir am “ <strong><em>W Reef </em></strong><em>” </em> tauchen. <strong><em></em></strong>M&ouml;glicher <strong></strong>Nachttauchgang “ <strong><em>Pasir Putih” </em></strong>. <strong><em></em></strong></p>
<p><span style="font-weight: bold; font-family: Verdana, Arial, Helvetica, sans-serif;">                                </span><strong>5. Tag: Komodo National Park <br>
</strong>Nach dem Fr&uuml;hst&uuml;ck cruisen wir zur Nachbarinsel <strong>Rinca </strong>in die <strong>Horseshoe Bay </strong>. “ <strong><em>Dive Yellow Wall </em></strong>”: Die ganze Wand ist &uuml;ber &amp; &uuml;ber mit <strong>gelben Weichkorallen </strong> &amp; <strong>Krustenanemonen </strong> bewachsen, daher der Name. Viele <strong>Schnecken </strong>, <strong>Drachenk&ouml;pfe </strong>, <strong>Seenadeln </strong> und <strong>Blennies</strong>. “ <strong><em>Dive Cannibal Rock </em></strong>” ist ein kleiner phantastisch bewachsener <strong>Unterwasserberg </strong>. <strong>See&auml;pfel </strong> und andere Vertreter der Seegurkenfamilie sind hier reich vertreten. Aber auch <strong>Oktopusse </strong>, <strong>Nacktschnecke n </strong>, <strong>Kugelfische </strong> und <strong>Blennies. </strong>“ <strong><em>Dive Pelican Head West </em></strong>”. &Uuml;berfahrt nach <strong>Gili Lawah Laut </strong>. <strong></strong></p>
<p><strong>6. Tag: Gili Lawah Laut <br>
</strong>Das <strong>Riff </strong> ist &uuml;berwachsen mit <strong>bronzenen </strong> &amp; <strong>rostfarbenen Schw&auml;mmen </strong> sowie <strong>Weichkorallenb&auml;umen </strong>. Man trifft auf <strong>Schnapperfische </strong>, gro&szlig;e <strong>Tintenfische </strong>, <strong>Falterfische </strong> und vereinzelte <strong>rote Oktopusse </strong>. “ <strong><em>Dive Crystal Rock </em></strong><em>” </em>, “ <strong><em>Dive Castle Rock </em></strong>”. Sp&auml;ter am Nachmittag spazieren wir auf den h&ouml;chsten Punkt der Insel um das herrliche Panorama der Mondlandschaft Komodos zu genie&szlig;en. Danach geht's Richtung <strong>Batu Moncho </strong>. </p>
<p><span style="font-weight: bold; font-family: Verdana, Arial, Helvetica, sans-serif;">                                    </span><strong>7. Tag: Batu Moncho <br>
</strong>Unser erster Tauchgang ist “ <strong><em>Moncho - Inner Bay </em></strong>” mit seinen <strong>au&szlig;erordentlichen Hartkorallen </strong>. W&auml;hrend wir unser Fr&uuml;hst&uuml;ck genie&szlig;en segeln wir nach <strong>Banta Island </strong> zum “ <strong><em>Banta </em></strong><strong><em>Bay </em></strong>”. Sp&auml;ter erkunden wir nochmals den “ <strong><em>GPS Point </em></strong>”. Sie werden staunen welche Meeresbewohner sich hier am Nachmittag im Unterschied zu unserem morgendlichen Tauchgang tummeln. Abfahrt nach <strong>Satonda </strong>. </p>
<p><span style="font-weight: bold; font-family: Verdana, Arial, Helvetica, sans-serif;">                                      </span><strong>8. Tag: Satonda &amp; Medang <br>
</strong>Morgens erreichen wir Satonda, genau die richtige Zeit “ <strong><em>Lake Entrance Bay </em></strong>” zu betauchen. Der Ausblick auf den wilden Strand Satondas l&auml;dt zu einem entspannenden Fr&uuml;hst&uuml;ck w&auml;hrend wir uns f&uuml;r unseren n&auml;chsten Tauchgang in <strong>Medang </strong> vorbereiten: “ <strong><em>Shark Reef </em></strong>”. Nun wird es Zeit wieder in Richtung Zivilisation aufzubrechen, zu unserem n&auml;chsten Stopp <strong>Tulamben/Bali. </strong></p>
<p><span style="font-weight: bold; font-family: Verdana, Arial, Helvetica, sans-serif;">                                        </span><strong>9. Tag: Ost Bali <br>
</strong>Fr&uuml;h morgens sind wir die ersten Taucher in Tulamben um das <strong><em>ber&uuml;hmte Wrack der SS Liberty </em></strong> zu erkunden. Aber auch die Spots “ <strong><em>Batu Kelibit </em></strong>” und “ <strong><em>Drop Off </em></strong>” <strong><em></em></strong>haben viel zu bieten. Unsere Crew bereitet ein <strong>tolles Abschieds-Dinner </strong>zu, bevor wir durch die Nacht zur&uuml;ck nach <strong>Serangan </strong> segeln. </p>
<p><span style="font-weight: bold; font-family: Verdana, Arial, Helvetica, sans-serif;">                                          </span><strong>10. Tag: Bali <br>
</strong>Wir genie&szlig;en ein tropisches Fr&uuml;hst&uuml;ck an Bord bevor wir um ca. 9:00 in Serangan/Bali <strong>Abschied von der Moana </strong> nehmen. </p>
