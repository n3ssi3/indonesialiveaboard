                            <h1 class="style90">Tauchen in Rajah Empat </h1>
                            <p class="style82">The cruise will visit the area of Raja  Empat -literally   &laquo;&nbsp;the 4 Kings&nbsp;&raquo;-referring to the 4 main islands of  the group-, an area   that acquired the reputation to offer the best diving of  Indonesia in   terms of fish quantity and diversity, &nbsp;underwater sceneries and   topography. &nbsp;It is also among the best top side scenery  that you can   find in the world, with rain forest on the edge of the coral reefs,    small mushroom shaped calcareous islands, etc&hellip;</p>
