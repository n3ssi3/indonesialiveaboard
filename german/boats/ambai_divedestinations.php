                            <h1 class="style90">Das Banggai Archipelago</h1>
                            <p>Our cruise is the only dive operation in this pristine archipelago   consisting of one hundred and twenty   islands, and offering hundreds of   miles of drop off and fringing reefs. Located at the border between   Banda Sea to the south, and Molluca Sea to the north, the Banggai   islands, qualified by the naturalist Alfred Wallace as the <strong>"Mother of all living coral reefs"</strong>, has numerous large reefs that support a very rich marine life.<br>
                                <br>
                              Many of the dives are on off shore reefs with vertical drop-offs down   to several hundred meters. These fantastic walls present overhangs and   cavities; the deeper parts (during the dive, 15-40 mts) are covered with   black coral and gorgonian fans, inhabited by tunas, schools of   trevallies, batfishes, turtles,  napoleon, wrasses, groupers, snappers,   and a few reef sharks. Going up, the wall coverage shifts to hard and   soft corals associated with a high density of the all range of reef   fishes such as Banner fishes, butterfly fishes, damselfishes, anthias   sp, dart fishes, dotty backs, and  the dives ends with a safety stop in   the middle of a gigantic aquarium.</p>
                            <p>Some diving sites with stronger currents offer more pelagic fishes such   as sharks, tunas, schools of giant trevallies; one submerged pinnacle   (top at -22mt) has an incredible density of big size fishes.</p>
                            <p><strong>Biodiversity</strong> is extremely high; fringing reefs of inner islands   offer all the range of the Sulawesi creatures: Anglerfishes, ribbon   eels, Blue- ring octopus, bob tail squid, Leaf scorpion fish, Ghost   pipefish, and of course the endemic ornamental <strong>Banggai Cardinal Fish</strong> that  can be observed in sheltered shallow waters.<br>
                                <br>
                                <strong>The best season</strong> to dive there is October to May; Banggai   Islands are well protected from the northwesterly winds that affect   other areas during January and February.<br>
                                <br>
                              Following successful exploration of <strong>new dive sites</strong> on the   eastern tip of the peninsula -that forms the second finger of Sulawesi-,   we recently revised the cruise itinerary. We now dive during the firsts   five days a selection of our best dive sites in Banggai islands, and we   spend the last three days on the peninsula, where dives offer a very   different scenery: rock formations often forming pinnacles with an   exceptional density of fishes, including barracuda schools, tunas, giant   jack fishes, sharks, eagle rays and many other species. Abundant   coverage of gorgonians, soft corals and daisy corals, make also these   dives very colorful.</p>
                            <h1 class="style18 style89">Die Togian Inseln</h1>
                            <p>From April to October, the cruises are located in the <strong>Togian Islands</strong>,   inside the large Tomini Bay. The Togians include 3 main volcanic   islands close together, and another one apart on the North that erupted   last in 1984, as well as numerous islets. <strong>It is the only place in the world where barrier reefs, atolls, and fringing reefs are found together.</strong> Most of the islands is still covered by dense tropical forest, human settlements and tourism are still very limited. <br>
                                <br>
                              The Togian islands dives specificity is certainly the beauty and the   diversity of the underwater sceneries their offer, with a visibility   that is generally very good.<br>
  <br>
                              Around the volcanic island of <strong>Una-Una</strong>, where we stay for 3   days, sponges size and shape diversity are astonishing, probably among   the biggest in the world. The fish life is very dense: schools of jacks   and barracudas, Bump head parrotfish's, numerous napoleon wrasses,   snappers, fusiliers, etc.<br>
                              Other dive sites include beautiful drop offs with crevasses and   overhangs, canyons, rocks formations, pinnacles, all offering a great   variety of spectacular underwater sceneries.<br>
  <br>
                              Pelagic life is present on some of the spots with schools of   barracudas, tunas, giant trevallies, eagle rays, reef  sharks and   sometimes hammerhead sharks.</p>
                            <p>The development and the diversity of coral formations are also a   highlight of the Togian islands. The cruise also includes a visit to an <strong>intact plane wreck B24 from the 2nd World War</strong> laying on the bottom at eighteen meters depth.<br>
                                <br>
                              Our twelve days cruise, EXT TOG, covers the 8 days Togian cruise and   ends in Luwuk, spending four more days of diving along the eastern tip   of the peninsula that separates Tominy bay from Luwuk area. The dive   sites of this area -we are the only dive operators there- are mostly   rocky formations and pinnacles with an intense marine life and a high   density of fishes including pelagic as tunas and giant jacks. Abundant   gorgonians, soft corals and daisy corals make also those dives very   colorful. For a more precise description of the Peninsula dive sites,   please refer to the Banggai Cruise section.</p>
