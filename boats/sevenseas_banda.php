                            <span class="style15">Seven Seas - Dive Destination Banda </span></p>
                            <p align="left"><strong>Banda Sea and Halmahera</strong><br>
                                <br>
                                Once sought by Columbus and Marco Polo, the Banda Islands were the original   Spice Islands and the most coveted destination on earth, particularly by the   Dutch and Portuguese who colonized the islands and exported the indigenous   nutmeg and cloves. History tells of a violent past under colonial rule. Now, the   seas around these tropical paradise islands are coveted for their spectacular   diving and snorkelling on some of the world's richest reefs, unspoiled corals,   and large pelagic fish.<BR>
                                <BR>
                                Steep drop offs, impressive hard coral and some   fast currents make this area absolutely breathtaking. Schools of jacks are a   familiar sight, as are large tuna, many turtles, Napoleon wrasse, groupers,   rays, sharks and large lobsters. Great visibility is a blessing here, and there   are also some special critter sites.<BR>
                                <BR>
                                The Banda Sea is surrounded by   islands from the large islands of Buru, Halmahera, Ambon and Seram in the north   towards Gorom, Kei and Aru in the east, the islands of Tanimbar, Wetar and   Reong, and a series of smaller islands such as Babar and Moa, touching East   Timor in the south. Ambon Bay is host to some of Indonesia's best critter   diving. The nearby island of Halmahera is still largely unexplored and has   recently produced some stunning sites.<BR>
                            </p>
                            <p align="center"><img src="sevenseas/banda_island.jpg" width="345" height="231"></p>
                            <strong>Gunung Api:</strong> an isolated volcanic peak rising from the heart of the Banda   Sea. Surrounded by crystal clear waters this uninhabited island is home to   thousands of nesting seabirds. Exuding sulphur both above and below the   landmark, the water is also home to extraordinary numbers of banded kraits (sea   snakes), which have become a special attraction for divers. It's not unusual to   have swarms of these non-aggressive sea snakes around you while diving on Gunung   Api.<BR>
                            <p align="left"><strong>Lucipara:</strong> these 5 tiny atolls in the middle of the Banda Sea are the tops   of undersea mountains rising up over a mile from the ocean floor. The   uninhabited beaches are ideal green turtle nesting sites, while the reef's   breathtaking drop off to ocean waters is ideal for diving and snorkeling. A   highlight here is night diving with the rare Photoblepheron bandanensis or more   commonly known as the "flash light fish".<BR>
                            </p>
