                            <h1 class="style18">Komodo National Park</h1>
                            <p><strong><em>Period: June to October</em></strong></p>
                            <p>Komodo national park is one of the greatest dive destinations, and   one of the most diverse and rich marine environments in the world.These   Small islands fringed with sandy beaches are home to the famous Komodo   dragon that you may see patrolling the beach or viewing you from a rock.</p>
                            <p>The coral reefs in the north of the park are rich with colorful   combination of hard and soft corals. Be delighted with the schools of   anthias, wrasses, majestic angelfish, and also large predators &ndash;reef   sharks, tunas, and giant trevallies-</p>
