                            <p><strong><em>Period: November to March</em></strong></p>
                            <p>The area known as Raja Ampat is an archipelago consisting in 4 main   islands surrounded by over 1500 smaller islands and cays. This area is   part of the West Papua province of Indonesia.</p>
                            <p>It is certainly the world's most biodiverse marine regionwith more   recorded fish, coral and mollusks species than anywhere else on the   planet. Some areas boost enormous schools of fishes ; there are spots   with large groups of manta rays, other with schools of barracudas,   trevallies, bump head parrot fish, large groupers, and of course always a   great variety of critters. The reefs are varied as their inhabitants,   with vertical walls, seamounts, pinnacles, slopes, caves, lagoons, some   more covered with hard corals and other displaying colorful soft corals.   Everywhere you will find a lot of gorgonians with their resident pygmy   seahorses. Everywhere you will find a profusion of colors and life.</p>
