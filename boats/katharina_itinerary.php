                              <span class="style54">Arrive at Pulau Bungin a tiny island, populated by &lsquo;sea-gypsies&rsquo; (bajao). We stroll through
                              narrow streets lined up by houses on stilts to outsmart the incoming tide. Performance of
                              traditional music and dance. Onward sailing to Pulau Bedil. Here we spend a few hours
                              relaxing, swimming and snorkeling. Night sailing to east-Sumbawa.</span><br>
                              <strong>Day 3</strong><br>
                              <span class="style54">Arrive in the morning at Sangean island, an active volcano towering 1800 meters above
                              the surrounding waters. We will visit the tiny settlement on the island and make a short hike
                              testing the steepness of the volcano<br>
                              Midday sail toward Wera on the east coast of the island of Sumbawa. Wera is a Buginese
                              settlement and famous for its boatbuilding. We will see all kinds of craft in various stages of
                              construction, much the way the Katharina was built in 1998. Afternoon sail to Gili Banta, a
                              perfect place for some excellent swimming and snorkeling.
                              Night-sailing to Rinca.</span><br>
                              <strong>Day 4</strong><br>
                              <span class="style54">Rinca island, the second biggest in the Komodo archipelago, is also the richest in terms
                              of fauna. We will go trekking and looking for wild horses, water buffalo, wild boar, monkeys,
                              and of course the famous &lsquo;Komodo dragons&rsquo; in spectacular scenery of savannah and
                              emerald coves. Around noon sail to &lsquo;Sabolon&rsquo;, a famous reef with a huge variety of live
                              coral and beautiful fish. Snorkeling, diving (optional)* and beach-exploration. In the
                              afternoon proceed to the Riung archipelago, north of Flores.</span><br>
                              <strong>Day 5</strong><br>
                              <span class="style54">In the morning arrival at Riung, a small archipelago of 17 islets, just north of the coast of
                              Flores. Snorkeling, relaxing, beach-combing, climbing one of the summits for enjoying the
                              panorama over the sea and Flores. Barbecue at desert beach In the afternoon sailing to
                              Ropa, a small harbour at the coast of Flores.</span><br>
                              <strong>Day 6</strong><br>
                              <span class="style54">At dawn arrival in Ropa from where we go by bus to the Kelimutu volcano, famous for its
                              three coloured lakes. Experience the spooky atmosphere surrounding this natural
                              phenomenon, quite unique in the world. Upon returning aboard the Katharina we set sail for
                              the town of Labuhanbajo at the westernmost point of Flores.</span><br>
                              <strong>Day 7</strong><br>
                              <span class="style54">If winds allow, the crew of the Katharina will hoist the sails and show their skills in
                              traditional Pinisi-sailing, a.o. by using the two side-rudders by turns.<br>
                              Late morning arrival in Labuhanbajo. Here we will tour to a nearby village to attend the
                              so-called caci (whip-) dance for which the Manggarai district of Flores enjoys national
                              fame. Midday sail to Bidadari island, swimming, snorkeling, exploring the interior.<br>
                              Afternoon sail to Komodo; spend a quiet night in the bay of Komodo island.</span><br>
                              <strong>Day 8</strong><br>
                              <span class="style54">Morning arrival in Komodo Island. Another opportunity to catch a glimpse of, or even
                              stumble over the last representative of the Jurassic Park on earth, the infamous Komodo
                              Dragon. Midday sail to Red Beach, another breathtaking desert island where the beaches
                              are red-coloured by pulverized red coral. In the afternoon visit of Gili Lawa to hike the
                              local summit and enjoy a splendid panorama over the moonscape islands of the Komodo
                              Archipelago and the mainland of Flores. Late afternoon setting sail, headed west.</span><br>
                              <strong>Day 9</strong><br>
                              <span class="style54">Morning arrive in Satonda, the top of an extinct volcano. We will visit the crater lake in the
                              centre of the island, where we may meet colonies of flying foxes after a short walk. An
                              afternoon of relaxation on the beautifully wild beach of Satonda and of lazy floating on the
                              gentle currents over a stunning reef drop just off the coastline. Afternoon sailing to Pulau
                              Kromo. Here we will have our farewell party with the crew, possibly witnessed by
                              thousands of fruit bats crossing to the mainland looking for night shelter. Night sailing to<br>
                              Lembar, southwest Lombok</span><br>
                              <strong>Day 10</strong><br>
                              <span class="style54">Early morning arrive at Lembar harbour..<br>
                              On the last dayof our cruise we will tour west-Lombok and visit a few original Sasak villages
                              where we will admire the local arts of pottery and hand weaving. Before noon we re-embark
                              the Katharina to set sail to Benoa harbour, southeast Bali.<br>
                              Arrival in Benoa before sunset. End of the cruise, individual transfer to hotel or airport.</span><br>
                              <strong>* Opportunity for optional diving; to be booked in advance.</strong></p>
