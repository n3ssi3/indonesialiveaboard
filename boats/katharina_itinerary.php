                            <p align="center" class="style43" style="margin-top: 0px;">FLORES SEA &ndash; BANDA - KEI<br>
                              Cruise
                              14 Days / 13 Nights</p>
                            <p align="left" class="style45" style="margin-top: 0px;"><strong>Day 1</strong><br>
                              10:15 hrs Flight Denpasar-Labuhanbajo Arrrival in Labuhanbajo at 11:30 hrs. Transfer<br>
                              from airport to the harbour, embarkation aboard the Katharina. Set sail to Bonerate.<br>
                              <strong>Day 2</strong><br>
                              Arrival at Bonerate in the middle of the Flores Sea. Visit of the main village. Sandy streets,
                              houses built on stilts in typical Makasserese style. On the beach construction of the elegant
                              Lambo boats. After inspection of the village, swimming, snorkeling and lounging on the
                              beaches at the northwest side of the island. Night sailing to Batuata Island, southwest
                              Sulawesi.<br>
                              <strong>Day 3</strong><br>
                              Arrival at Pulau Batuata, southwest Sulawesi. After setting foot ashore, we pay respect to
                              the local Chief, ask for permission to go around and see the island. We visit the local school,
                              hear the children singing the latest local hits, drink tea in the &lsquo;cityhall&rsquo; and experience the
                              remoteness of this place destitute from the comforts of modern life. In the evening we sail
                              onward to the Tukang Besi archipelago, southeast Sulawesi.<br>
                              <strong>Day 4</strong><br>
                              Arrival in the Tukang Besi archipelago, world famous for its pristine coral reefs which<br>
                              belong to the most diverse in Indonesia. For two days we will wander about this unique<br>
                              geographical area inhabited by a tribe of daring seafarers, shipbuilders and maritime traders
                              We will look for small coves and inlets, the best soft corals, meet fishermen, local
                              blacksmith (tukang besi = blacksmith), visit local markets and may come across the perfect
                              location for a barbecue.<br>
                              <strong>Day 5 </strong><br>
                              Extension of Day 4. Late afternoon departure headed for Gunung Api.<br>
                              <strong>Day 6</strong><br>
                              In the course of the day arrival at Gunung Api (fire mountain/volcano) in the middle of the
                              Banda Sea. Here we hope to meet a mixed colony of seabirds, predominantly Frigate Birds
                              and John-of-Gaunts. The Birds may carry out raids on the Katharina if they feel threatened
                              in their habitat. In the evening onward sailing to the Lipari archipelago.<br>
                              <strong>Day 7</strong><br>
                              Arrival at the Lipari archipelago, a volcanic outcrop in the north eastern part of the Banda
                              Sea. As is the case with Gunung Api, no reports have come in from the Lipari since the late
                              nineties when ornithologists mentioned colonies of migrating birds and groups of migrating
                              fishermen. A destination of discovery in the true sense of the word! <strong>Day 8 </strong><br>
                              Spare day. Night
                              sail to Banda.<br>
                              <strong>Day 8</strong><br>
                              Arrival in the Banda Archipelago, famous for its natural beauty and its cultural heritage<br>
                              from the time the Spice Islands were the target of the European colonizers. We will tie the
                              bow to a palm-tree in the garden of a hacienda style hotel in Bandanaira the only (small)
                              city of Banda. We will stroll through the city, visit old fortresses and admire the planters&rsquo;
                              mansions from the old days.<br>
                              <strong>Day 9</strong><br>
                              Today we will visit a few of the other islands of the archipelago, Ai, Revenge and Hatta<br>
                              and will see what remains of the ancient plantations, churches and cemeteries. We will<br>
                              breathe the atmosphere of the time that the Banda youth was sent to the most expensive<br>
                              boarding schools of Europe and dream about the splendid, but short-lived glory during this
                              remarkable episode in world history. Proceed from Banda northeast.<br>
                              <strong>Day 10</strong><br>
                              Today we&rsquo;ll stop at the northern side of the Tanimbars, at the islands of Fordate and<br>
                              Larat. Village exploration, nature hike and snorkelling will be the substance of the day.<br>
                              With the assistance of local teachers we will try to attend to a performance of local music
                              and dance. Late afternoon onward sailing to the Kei Archipelago.<br>
                              <strong>Day 11</strong> Leisure day<br>
                              <strong>Day 12</strong><br>
                              Arrival in Manawoka, one of the Gorong islands, southeast of Ceram. After a short walk<br>
                              through and old nutmeg grove and a visit to the raja of the island we move to the eastern<br>
                              tip of the island. There we find two lakes connected with the sea by a small tunnel.<br>
                              Swimming in these lakes evokes the feeling of discovering an unknown planet.<br>
                              <strong>Day 13</strong><br>
                              Today we will arrive at the Kei Archipelago. We will visit on the last day of our cruise Kei<br>
                              Besar. Here we will make a short trek into the forest where Sir Alfred Wallace 160 years<br>
                              ago was collecting his bird and insect species. We may come across a small village where
                              nice pottery is made. Night alongside the pier in Tual, Kei Kecil.<br>
                              <strong>Day 14</strong><br>
                              Early morning transfer to the airport of Langgur for your flight at 06:30 hrs. to Ambon,<br>
                              from where the onward flight to Makassar will depart at 14:05 hrs. End of the cruise.</p>
