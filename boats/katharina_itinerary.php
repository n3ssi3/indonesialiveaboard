                            <p align="center" class="style43" style="margin-top: 0px;">KEI &ndash; TANIMBAR - TIMOR<br>
                              Cruise
                              12 Days /11 Nights</p>
                            <p align="left" class="style45" style="margin-top: 0px;">Although Indonesia encompasses 18.000 odd islands, offering a stunning variety of culture, flora, fauna
                              and marine biology, voyages overseas, hopping from one island to the next, are virtually unheard of.<br>
                              Indonesia boasts the largest wooden fleet still in active operation: the Bugis Schooners or Phinisi.
                              These flat bottomed vessels need little maritime infrastructure as their construction allows them to
                              approach even the tiniest islet at close range.<br>
                              Sea Trek has used these time honoured craft since 1985 to visit islands unknown to the world.
                              This voyage to the eastern Lesser Sunda&rsquo;s and the Southest Moluccas will bring you to the most remote
                              parts of the archipelago, to places over and below the sea level you haven&rsquo;t dreamt of existing.<br>
                              Gradually passing from Asia to the Pacific you will set foot ashore on a string of islands stretched out
                              along the Ring of Fire, in our time a backwater but for centuries the pot of gold at the end of the
                              rainbow where the weight of nutmeg and clove was measured in gold bars.</p>
                            <p align="left" class="style45" style="margin-top: 0px;">Prior to the cruise flight Denpasar-Ambon (via Makassar).<br>
                              Transfer to hotel Ambon Manise where we will spend one night. After check in visit of Siwa
                              Lima Museum in the hills outside Ambon City where we can study the history and culture of
                              the Mollucas.<br>
                              <strong>Day 1</strong><br>
                              After spent the night in Ambon, flight Ambon-Kei, dep.07:00 arr.09:05*. Upon arrival at<br>
                              Langgur airport, Tual transfer to the harbour of Tual and embark aboard Katharina. We<br>
                              settle in and after dinner we will explore Tual, the capital of Kei. Late evening departure to
                              Kei-Tanimbar.<br>
                              <strong>Day 2</strong><br>
                              Early morning arrival at Kei-Tanimbar, the southernmost island of the Kei archipelago and
                              allegedly cradle of the regional culture. Here we will explore coastal villages, search for
                              cultural artefacts such as weaving and pottery or make a trekking into the local forest. Late
                              afternoon onward sailing to the Tanimbar Archipelago. Long night sailing.<br>
                              <strong>Day 3</strong><br>
                              We&rsquo;ll stop at the northern part of the Tanimbars, at the islands of Fordate and Larat.<br>
                              Village exploration, nature hike, beach-combing and snorkelling will be the substance of the
                              day. With the assistance of local teachers we will try to attend to a performance of local
                              music and dance.<br>
                              <strong>Day 4</strong><br>
                              Following the northern coastline of Yamdena, the main island of the Tanimbar archipelago,
                              we will meet along the western coast islands of quiet beauty with untouched forest teeming
                              with birds, wild orchids and cuscus. This will be another day of nature exploration, coastal
                              raids with our zodiac, and some relaxing time on remote beaches. Night sailing to Dai,
                              Babar.
                              <br>
                              <strong>Day 5</strong><br>
                              The island of Dai &ndash; in the Babar archipelago &ndash; is a spectacular limestone mountain<br>
                              towering 650 meters above the sea. The beaches and the reefs are exceptional. We can<br>
                              also trek across the whole island to enjoy incredible scenery from the summit. Local folk still
                              go hunting with bows and arrows in these parts. Night sailing to the island of Damar.<br>
                              <strong>Day 6</strong><br>
                              The island of Damar has an active volcano that can be ascended in about three hours, best
                              in the early hours after dawn. Alternatively, we can have an easier hike through spice
                              plantations, coconut groves and pristine rain forest, ending at the local village.<br>
                              Midday sail for a couple of hours to uninhabited Pulau Terbang Utara to enjoy pristine<br>
                              beaches, coral reefs (snorkelling) and a lagoon full of birds, including &ndash; occasionally &ndash; large
                              flocks of pelicans. In the evening barbecue at the desert beach at this Bounty island.
                              Night sailing heading for Romang.<br>
                              <strong>Day 7</strong><br>
                              Morning on uninhabited Moapora island, in the Romang archipelago: beach combing,<br>
                              snorkelling, diving and short hikes inland. Afternoon, we&rsquo;ll be on Romang island proper for a
                              three hour hike (if we wish) from the eastern village to the beautiful western bay passing
                              through orange and pomelo groves with great scenery on surrounding islands. Night sailing
                              to the island of Wetar.<br>
                              <strong>Day 8</strong><br>
                              Wetar is possibly one of the least known of the south-eastern Maluku islands; totally<br>
                              isolated tribal folk is still said to inhabit its jungle-clad interior. We&rsquo;ll have a nature stop in
                              the morning along the northern coast: hike inland following a river of crystal clear water.
                              Midday sail to the western tip of Wetar for spectacular snorkelling close to a small offshore
                              island. Night sailing, heading for Alor.<br>
                              <strong>Day 9</strong><br>
                              Our day in Alor will be spent exploring the main island: the old tribal villages there are<br>
                              defensively located on hilltops and have a particular architecture. We&rsquo;ll attend traditional<br>
                              dances in the village of Takpala and will be surprised by the redoubt appearance of the local
                              women, the size of pygmees. Return to vessel at midday and sail to nearby Pura island: you
                              can enjoy spectacular snorkelling right off the beach, and also walks to coastal villages
                              where locals produce ikat cloth as well as some wine made from palm sap. Short night
                              sailing to Pantar.<br>
                              <strong>Day 10</strong><br>
                              On Pantar island we will visit some of the fortified villages typical for this area once feared
                              for its belligerent population. We will be peacefully guided by one of our crew who hails
                              from Pantar. Be prepared for some interesting expressions of local culture!<br>
                              Upon re-embarkation Katharina, we will be swimming in crystal clear waters around the<br>
                              boat. Midday sail to uninhabited Rusa island: the beaches are truly fantastic, the corals<br>
                              exquisite and the island &ndash; as its name implies &ndash; is full of deer that are easily seen,<br>
                              sometimes even from the beach. Night sailing.<br>
                              <strong>Day 11</strong><br>
                              Exploration of the coast of west-Timor, moving south for Kupang. We will search for local<br>
                              fishing communities and vestiges of Portuguese occupation, making one or two snorkelling
                              stops on the way. Night sailing to Kupang.<br>
                              <strong>Day 12</strong><br>
                              After arrival this morning in Temau, the traditional harbour of Kupang, we will take our<br>
                              breakfast on board and say goodbye to the crew and the Katharina. Time allowing we will<br>
                              pay a visit to the Museum of Timor on the way to the airport. From Kupang airport we will<br>
                              have our return flight to Denpasar, Bali.<br>
                              *Due to the early departure of this flight, one night overstay in Ambon.</p>
