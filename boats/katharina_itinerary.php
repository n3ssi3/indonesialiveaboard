                            <p align="center" class="style43" style="margin-top: 0px;">TIMOR &ndash; ROTI &ndash; SAVU &ndash; KOMODO - BALI<br>
                              Cruise
                              10 Days /9 Nights</p>
                            <p align="left" class="style45" style="margin-top: 0px;"><strong>Day 1</strong><br>
                              Arrival by air in Kupang, West Timor. After our transfer from the airport to the harbour and
                              embarkation aboard Katharina, we will set sail to the nearby island of Semau. Here we will
                              have a stroll on the long white sandy beaches, do some snorkeling or hike to an inland
                              village. Night sailing to the island of Roti.<br>
                              <strong>Day 2</strong><br>
                              Arrive in Baa, the capital of Roti Island. Roti has been long under the influence of<br>
                              Portugal, which is demonstrated by the local handicraft. Here is your chance to buy silver
                              ornaments, distinctive Rotinese ikats, and pinnacled hats plaited from lontar leaves. We may
                              be entertained by music and dances produced by the sasando, a 12-string lontar-leaf harp
                              originating from this tiny island. The music is reminiscent of the Fado music and the
                              Saudade sentiments of Portugal. Evening sail to the island of Savu. If time allow we will
                              attend a music and dance performance.<br>
                              <strong>Day 3</strong><br>
                              Arrive in Sepa, Savu. Here we may be greeted by ikat-clad tribesmen on horseback who<br>
                              might escort us to the sacred gathering place of the elders of Savu, which only a few<br>
                              outsiders are allowed to visit. We&rsquo;ll ask for permission from the local elders to visit this<br>
                              place. The impressive circle of stones commands respect to the wise men which ensure the
                              continuance of timeless adat traditions. The ikat of Savu are some of Nusa Tenggara&rsquo;s finest
                              and there will be opportunities to shop for them. Admire the female of Savu who are
                              reputed in the whole archipelago for their beauty. We wander by foot or on horseback
                              about the interior of Savu, breathing the timeless atmosphere of the island, visited and
                              admired by Captain Cook and his crew. Late afternoon we sail onward to either the island of
                              Raijua or to Sumba.<br>
                              If sea conditions allow, we make a stop in Raijua island; as everywhere in the Savu<br>
                              area, it is, for the casual visitor, a return to a strictly rural, animistic society, with all<br>
                              villages built and organised in a strictly traditional way, giving everyone a sense of<br>
                              being back in a sort of garden of Eden; expect Midday sail to uninhabited Dana island for a
                              Robinson Crusoe experience and an beach BBQ in splendid isolation . If conditions do not
                              permit we will sail onward straight to the island of Sumba.<br>
                              <strong>Day 4</strong><br>
                              If Raijua was skipped we will arrive today in Waingapu, the capital of Sumba. After a<br>
                              short inspection of the bustling centre, we will make a bus ride (non-AC) to the traditional
                              villages of Rende and Prailiu, where we will admire the intricate and long-winded art of ikat
                              weaving. The local population is stark and dark and inspires a strong sensation of being half
                              way Asia and Polynesia. Upon re-embarkation night sail along the north coast of Sumba to
                              from Waingapu to Waikelo. West-Sumba.<br>
                              <strong>Day 5</strong><br>
                              Arrival at Waikelo, West Sumba. Today we will visit by local bus (non-AC) some of the<br>
                              traditional villages of West Sumba, where time seems to have lost direction and life appears
                              not to have changed for ages. We will admire the megaliths erected on ancestral tombs and
                              will have time to shop for ikat weavings, the distinctive Sumba motifs stunning the modern
                              visitor. Along the way we will find a place to picnic and eat our lunchbox prepared by the
                              crew. Late afternoon re-embarkation aboard Katharina and departure heading north for the
                              Komodo Archipelago. Where our first stop will be on Rinca Island.<br>
                              <strong>Day 6</strong><br>
                              Early morning we will trek on Rinca Island, the second largest island of the Park, offering<br>
                              a wider variety of wildlife than Komodo Island: wild horses &amp; water buffalo, feral deer &amp;<br>
                              boars, rare megapode birds, and of course the famous Komodo dragons. Rinca&rsquo;s<br>
                              unsurpassed savannah scenery is accentuated by deep blue coves and inlets. At midday<br>
                              we&rsquo;ll sail to Padar Island for an afternoon swim and snorkelling or a short trek up to the<br>
                              summit of the grassy hill for superb panoramas of the surrounding islands. In the evening,
                              we&rsquo;ll barbecue on a deserted beach under the blinking gaze of a million stars.<br>
                              <strong>Day 7</strong><br>
                              Early morning another opportunity to spot the giant Komodo Lizard. After a short trek into
                              the interior (45 min.) of Komodo island proper, we will almost stumble over the<br>
                              bloodthirsty creatures, who likely will cross our path. After this meeting we will do<br>
                              swimming and snorkeling over one of the best reefs of the area at the so-called &lsquo;red beach&rsquo;.
                              Night sailing to Wera, east-Sumbawa.<br>
                              <strong>Day 8</strong><br>
                              Arrival in Wera, a traditional east-Sumbawanese village famous for their boat-building We
                              will see all kinds of craft in various stages of construction under the palm trees on the
                              beach. The same way the Katharina was built in 1998. Around noon sailing to Sangeang
                              Island, a volcano towering 1600 meter over the sea. After a short visit of the local village,
                              where we will wonder how people manage to survive, we will head west to islands north of
                              Sumbawa.<br>
                              <strong>Day 9</strong><br>
                              Arrival at Satonda island, the tip of an ancient volcano; here we see the huge crater-lake<br>
                              just a few minutes from the beach. Reefs around the island are spectacular and mostly in<br>
                              pristine conditions, excellent for snorkelers. Before noon sailing to Moyo. Upon arrival at<br>
                              Moyo, we will hike through the monsoon forest to a lovely three-tiered waterfall for a dip or
                              a swim in complete wilderness. Later afternoon onward to Lombok.<br>
                              <strong>Day 10</strong><br>
                              Early morning arrival at the harbour of Lembar, southwest Lombok.<br>
                              From here we will make a tour over west-Lombok, visiting a few original Sasak villages<br>
                              where we will admire the local arts of pottery and hand weaving. Before noon we re-embark
                              the Katharina to set sail to Serangan (turtle island). Serangan is connected with the
                              mainland of Bali.<br>
                              Arrival in Serangan before sunset. End of the cruise and individual transfer to your hotel.</p>
