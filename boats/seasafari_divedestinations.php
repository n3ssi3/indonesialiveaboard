                            <h3>Misool - Irian Jaya</h3>
                            <p>The Misool Island group sits at the entrance of the Seram Sea, two   degrees south of the equator. This entire region is one enormous ancient   limestone basin and even has limestone as the sea floor. Those who have   traveled to Palau can picture these little tropical islands covered in   pandana and other palms, with steep sided walls where wind and sea has   worn the rock away.                            </p>
                            <p>The islands, or islets, are uncountable. The larger ones, like Misool   itself, have canyons and lagoons speckled with white sand beaches ideal   for tender boat rides. One can spend hours exploring and never see the   same bay. It really is a place of great beauty both above and below the   water. It's rightly being considered by the Indonesian government for   national park status and by the United Nations as a world heritage site.</p>
