                                          <span class="style15">The Phinisi Schooner  &quot;Sea Safari VIII&quot; </span></p>
                                        <p align="left">The KLM Sea Safari VIII is one of Indonesia's largest and most spacious   luxury Phinisi liveaboard. One of the very few dive vessels in Indonesia   built according to international marine class specifications, the ship   spans an impressive 44 meters long and 10 meters wide providing guests   with an abundance of space and luxury. The Sea Safari VIII is our new   dedicated vessel for all scheduled cruises in the Far East of the   Indonesian Archipelago including the Raja Ampat region.</p>
                                        <p>Indonesian 'Bugis Prahus', commonly known as Phinisi, are made from   locally grown timber according to a tradition that dates back to long   before Portuguese colonists reached the area. The classic beauty and   elegance from the Sea Safari VIII combines this traditional   craftsmanship with modern conveniences, providing our guests with   luxurious comfort even in the remotest waters of the Indonesian   archipelago. The Sea Safari VIII is Indonesia's ultimate luxury   liveaboard scuba diving vessel.</p>
