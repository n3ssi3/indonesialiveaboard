                        peninsula -that forms the second finger of Sulawesi-, we recently revised the <br>
                        cruise itinerary. We now dive during the firsts five days a selection of our best <br>
                        dive sites in Banggai islands, and we spend the last three days on the peninsula, where dives offer a very different scenery : rocks formations often forming <br>
                        pinnacles with an exceptional density of fishes, including barracuda schools, <br>
                        tunas, giant jack fishes, sharks, eagle rays and many other species. Abundant coverage of gorgonians, soft corals and daisy corals, make also these dives <br>
                        very colorful.<BR>
