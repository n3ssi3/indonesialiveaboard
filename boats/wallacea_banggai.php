                    clumps of black corals, surrounded by Bat fishes and surgeon fishes schools. Overhangs and cavities often squatted by white tip sharks, schools of jacks fishes. After about 10-25 minutes at 100 feet, you slowly ascend following the wall. Dives <br>
                    on walls often last 70 minutes.<BR>
                    <BR>
