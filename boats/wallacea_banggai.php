                    beautiful formation of hard corals and sponges until 45 to 55 feet and the <br>
                    visibility is always excellent. The top of the reef, on depth from 25 to 10 feet, <br>
                    offers large areas of pristine coral formations of all shapes, endless fields of <br>
                    delicate branched Acropora species covered by clouds of damsel fishes, <br>
                    combinations of tables of Acropora. Generally divers stay underwater until <br>
                    90 minutes or more such is great the fascination of the scenery.<BR>
                    <BR>
                    <B>Kokunan bay (Banggai) : depth 35 feet</B><BR>
                    A fringing reef that hosts numerous Banggai Cardinal Fishes. This beautiful fish, endemic from Banggai islands, is one of the few marine fish without any larval <br>
                    stage. The male holds the eggs, then the embryos, in his mouth during a total incubation time of about 30 days. It lives in shallow water, in groups generally in association with urchins or anemones, and is a must for photographers. On this <br>
                    site, you can also see Mandarin fishes.<BR>
