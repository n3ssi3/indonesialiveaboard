                    The fringing reef of an islet of Banggai, with a slope covered by hard corals <br>
                    formations from 45 to 3 feet. Abundant critters: ruban eels, stone fishes, <br>
                    ghost pipe fishes, mantis shrimps. A site also very nice for night diving.<BR>
                    <BR>
                    <B>Kebun laut (Banggai): depth 54 feet.</B><BR>
