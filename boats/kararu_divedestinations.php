                                <br>
                                <span class="style18"> The Komodo Marine Park </span><br>
                                <br>
  The Komodo National Park, officially declared a World Heritage Site in 1991, encompasses 132,000 hectares of marine waters making it one of the largest protected zones on the planet. The number and variety of islands which include Rinca, Padar, Komodo and several smaller isles, combined with current, wave action and wind exposure, result in an incredible variety of coastal and marine habitats.</p>
