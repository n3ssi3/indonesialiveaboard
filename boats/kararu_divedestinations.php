                                <span class="style18">Alor Island</span><br>
</p>
                            <p>The Alor Island regency, in the far East of Indonesia, comprises two main islands Pantar and Alor, as well as a number of smaller isles. The undeveloped Alor <br>
  regency is home to just 2 percent of Indonesia's population. The Alor Straight <br>
  is one Indonesia's best kept secrets and a first class, undiscovered scuba diving region.</p>
                            <p>The water's surrounding the Alor regency are extremely rich with varied reef <br>
  profiles; walls, seamounts, rocky outcrops, and pristine coral-reefs. The secret <br>
  dive site in the Kalibahi Bay is home to harliquin shrimp, sea horses, ghostpipe <br>
  fish, wonderpus, mimic octupus and hairy frogfish.</p>
                            <p>Sustainable fishing practices and eco tourism are prevelent in the Alor Island <br>
  region and there are no signs of reef destruction. Opportunities abound to <br>
  interact with the locals on the various island who are a traditional seafaring <br>
  people. Visiblity is clear, waters warm and the density of divers is minimal, <br>
  Alor is perfect tropical scuba diving.<br>
  <br>
                            </p>
                            <p><br>
                                <span class="style18">Irian Jaya and Raja Ampat Islands </span><br>
                                <br>
  Irian Jaya comprises the western half of the world's second largest island, New Guinea. Irian Jaya is a true patchwork of ethnicities and one of the last great unexplored regions of our precious earth. Twenty percent of Irian Jaya's land <br>
  and marine parks are designated conservation areas yielding the highest fish <br>
  and coral count of any sea.</p>
                            <p>Marine biodiversity is evident everywhere rivaling any tropical rainforests in <br>
  species density. Irian Jaya has a tremendous variety of creatures on the reefs.<br>
  From over 3000 recorded fish species, 300 scleractinian (reef building corals), <br>
  100 sponge species, dolphins, dugong, hawksbill (Ertmochelys imbricata), green (Chelonia) and leatherback turtles (Dermochelys coriacea) who lay their eggs in Irian Jaya.</p>
                            <p>Fewer than two million people live in Irian Jaya's 410,660 square kilometers. <br>
  Unique flora and fauna abound including the bird of paradise, the giant <br>
  cassowary bird and bird winged butterlies Idyllic islands finged with white sand beaches, dotted isles filled with hanging orchids and venus fly traps, Irian Jaya's western tip holds some truly stunning regions both above water and below. </p>
