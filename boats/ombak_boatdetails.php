                            <div>
                              <p>Ombak Putih's main deck is dominated by the   25m tall mainmast and a large deck table where you can have dinner under   the stars. On the deck house level on the aft side of the boat, a   spacious sun deck (with canopy for some shade) overlooks the sea. Here   you can read in a comfortable deck chair or dream away on one of our   comfortable day beds.Your accommodation is below decks, where you will   find twelve cabins, four with double beds and four configured as   twin-bunks and four configured as triple, with a double and an extra   singe bed. Unlike typical ship accommodation, your cabin provides ample   space to store your stuff and still move about with ease.&nbsp; </p>
                              <p>All cabins are air-conditioned (individually controlled) and have   private bathrooms with warm and cold water. Obviously, we provide all   hotel amenities you may require.<br>
                                We know from experience that during a   cruise in the tropics some of our guests discover the pleasure of   sleeping under the stars and there is plenty of room to sleep on the   upper deck. Mattresses as well as light blankets are available for that   purpose.</p>
                              <p>The Ombak Putih is a mix of traditional and modern design. Her hull   and rigging are traditional, while the deck house and interior were   custom designed to adapt to Western tastes of space, privacy and   comfort. While Indonesian traditional sailing vessels are customarily   referred to as Bugi Schooners, strictly speaking she is rigged as a   Ketch, the foremast being higher than the aft mast.                            </p>
