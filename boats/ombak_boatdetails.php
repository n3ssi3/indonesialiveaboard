                          <td height="240" colspan="2"><p><strong>Construction and Design Parameters</strong></p>
                            <p>The construction followed age old and well established design   principles. First, traditional methods were use to determine the final   dimensions of the ship. Throughout Asia the Buginese people from the   island of Sulawesi are well- known for their shipbuilding skills and   what is most remarkable is that this huge hand made structure (36m   length, a beam of 10m and a draft of 3,5 m) was made without any   drawings, fully relying on the intuitive craftsmanship of its builders   as passed along through many generations. A Dutch naval architect was   hired to recalculate and verify all dimensions for ultimate   seaworthiness. He designed the interior and the deck house.</p>
                            <p>A second design principle we insisted upon was that we wanted the   ship to be as safe as possible. This meant that only first quality   timber was used and therefore the hull is considerably thicker than most   other wooden vessels (ranging from 10cm down below to 6cm near the   deck). The entire hull from the keel up to the decklevel was made from   carefully selected and seasoned ironwood (ulin). Other safety measures   include a 'work horse' Detroit Diesel engine, a brand that has proven   its durability and reliability over the last fifty years (most ships of   the US Navy have Detroit Diesel engines), anchor chains instead of wire   rope, and many safety features demanded by the Australian coast guard   for ocean going vessels.</p>
                            <p>A third design principle was that we wanted ample space for both   guests and crew. We designed and built a large deckhouse that was low   enough to maintain the original sail plan, while still providing   sufficient sleeping and living quarters for the crew. There is a   spacious lounge and a large galley. Behind the bridge on top of the deck   house, there is a large sun deck, partly covered by an awning to also   provide some necessary shade under the tropical sun.</p>
                            <p><br>
                              <strong>Safety and Seaworthiness</strong></p>
                            <p>The vessel is fitted with all internationally required safety and   communications equipment such as SSB and VHF radios, life jackets, life   rafts, and satellite telephone. Experienced navigators consider it an   axiom that the seaworthiness of a vessel depends on the seaworthiness of   the crew. Our ship has a well trained and well seasoned Indonesian crew   of sixteen, complemented by a multilingual European Cruise Director. We   are proud of the fact that most of the crewmembers that joined us when   the vessel was launched are still on board today.</p>
