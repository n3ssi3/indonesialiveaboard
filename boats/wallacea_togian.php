                            <div align="left"><BR>
                              From April to October, the cruises are located in the <B>Togian Islands</B>, inside the <br>
                              large Tomini Bay. The Togians include 3 main volcanic islands close together, and another one apart on the North that erupted last in 1984, as well as numerous <br>
                              islets. It is the only place in the world where barrier reefs, atolls, and fringing reefs <br>
                              are found together. Most of the islands is still covered by dense tropical forest, <br>
                              human settlements and tourism are still very limited. <BR>
                              <BR>
              The Togian islands dives specificity is certainly the beauty and the diversity of the underwater sceneries their offer, with a visibility that is generally very good.<BR>
              <BR>
              Around the volcanic island of Una-Una, where we stay for 3 days, sponges size and shape diversity are astonishing, probably among the biggest in the world. The fish <br>
              life is very dense: schools of jacks and barracudas, Bump head parrotfish's, <br>
              numerous napoleon wrasses, snappers, fusiliers, etc.<BR>
              <BR>
              Other dive sites include beautiful drop offs with crevasses and overhangs, canyons, rocks formations, pinnacles, all offering a great variety of spectacular underwater sceneries.<BR>
              <BR>
              Pelagic life is present on some of the spots with schools of barracudas, tunas, giant trevallies, eagle rays, reef sharks and sometimes hammerhead sharks.<BR>
              <BR>
              The development and the diversity of coral formations are also a highlight of the Togian islands. The cruise also includes a visit to an intact plane wreck B24 from the 2nd World War laying on the bottom at eighteen meters depth.<BR>
              <BR>
              Our twelve days cruise, EXT TOG, covers the 8 days Togian cruise and ends in <br>
              Luwuk, spending four more days of diving along the eastern tip of the peninsula <br>
              that separates Tominy bay from Luwuk area. The dive sites of this area -we are <br>
              the only dive operators there- are mostly rocky formations and pinnacles with an intense marine life and a high density of fishes including pelagic as tunas and <br>
              giant jacks. Abundant gorgonians, soft corals and daisy corals make also those <br>
              dives very colorful. For a more precise description of the Peninsula dive sites, <br>
              please refer to the Banggai Cruise section.<br>
              <BR>
              <BR>
                <BR>
                <B>Examples of diving sites we visit during the Togian cruise:</B>                <BR>
               Most of the dive sites that are walls or slopes can be dive at different depth <br>
               according to the diver's level and wishes, and if the dive is a morning or an <br>
               afternoon dive. That's why we sometime mention a depth range rather than a maximum depth.<BR>
              <BR>
                            </div>
                            <DIV class=text2>
              <p><B>Dominik rock; Kadidiri Isl., depth 110 feet</B><BR>
                It is a rocky formation outside the barrier reef (the barrier reef runs along almost <br>
                all the north coast of Togian islands). We leave the bottom of the reef wall at a <br>
                depth of 55 feet and follow the sandy bottom in between massive rocks, where we frequently spot giant groupers. The last rock, at 100 feet depth, makes a wall on <br>
                its outside part falling in the deep blue, from where are coming up tunas and <br>
                schools of jacks. We then follow the top of this rock that forms a flat platform <br>
                covered with colorful soft corals, sponges and splendid huge gorgonians. The <br>
                dive then go back to the reef wall that we follow, getting slowly up until the lagoon where we finish the dive.<BR>
                <BR>
                <B>Fishomania; Una-Una Isl., depth 40 - 80 feet</B><BR>
                Una-Una is a volcanic island in the North of the Togian group. The volcano last <br>
                eruption was in 1984, and since then human settlements are theoretically <br>
                forbidden on the islands. In fact, a few inhabitants stay there, taking care of <br>
                the numerous coconut trees on the lower part of the island, the upper part being covered by dense tropical forest.<BR>
                <BR>
                <B>Fishomania</B> is a pinnacle with a top a 12 feet deep, where aggregates high <br>
                densities of fishes: we generally spend a few minutes hanging on some rocks <br>
                in the current, looking at the non remitting and hypnotic ballet that forms <br>
                schools of fusiliers, surgeons fishes, snappers, jack fishes and so many others. <br>
                The outside part of the pinnacle forms a splendid wall covered by dense coral formations of all shapes and colors, associated with sponges of uncommon <br>
                size and shape.<BR>
                Turtles and Napoleons are usually spotted, as well as eagle rays and white tips <br>
                sharks.<BR>
                <BR>
                <B>Manara 1, Una-Una Isl., 45 to 90 feet</B><BR>
                On the Una-Una barrier reef, it is a slope offering a luxuriant hard coral coverage. Another highlight of this dive is a white gorgonian field at 90 feet depth, at the beginning of the dive. Same as on the other sites of Una-Una, you will find a <br>
                dense fish life all along the dive.<BR>
                <BR>
                A<B>pollo reef, Una-Una isl, 90 feet</B><BR>
                A reef formation on a sandy slope, from 25 to 35 meters depth. As on the other <br>
                dives on Una-Una, there is always a lot of fishes (schools of surgeons, fusiliers, Napoleon wrasses) but the highlight of the dive is huge schools of barracudas <br>
                (more than one thousand ?) that comes to check and surround the divers.<BR>
                <BR>
                <B>Kololio, Una-una isl, 30 feet Night dive</B><BR>
                We mention this night dive because in addition to the critters, we generally see <br>
                turtles, sleeping parrot fishes -including bump head parrot- wrasses, and many <br>
                other fishes interesting for portraits.<BR>
                <BR>
                <B>Mimpi Ding-Ding, Pasir Tengah atoll, depth 65 to 100 feet</B><BR>
                You dive straight down along an absolutely vertical wall that goes down to abyssal <br>
                depth (but not you, even though the temptation is hard to resist!). The deep and transparent blue of the water -the visibility is generally excellent- gives to this <br>
                dive a special fantasy ambient. This the archetype of the "wall", where hang <br>
                huge tubes and barrel sponges , black corals, where are cruising tunas, eagle <br>
                rays, ascending and descending schools of Platax and trevallies. The wall also <br>
                presents beautiful overhangs and crevasses. This a also a typical dive profile <br>
                of the tropics where you slowly get up along the wall and finish your tanks at <br>
                the top of the reef looking at the aquarium.<BR>
                <BR>
                <B>Goa-goa, Pasir tengah atoll, depth 55 feet</B><BR>
                Another wall, with a lot of overhangs and horizontal faults. A very good site for photographers, with a good coverage of soft corals. A large swim through at <br>
                40 feet deep, with walls and ceiling covered by bright yellow siphonogorgias. <br>
                The wall between 6 and 3 meters, where we spend the last twenty minutes of <br>
                the dive, is colonized by an amazing quantity of various nudibranches.<BR>
                <BR>
                <B>Batu Gila ("Crazy stone"), 100 feet</B><BR>
                A special spot for pelagics outside the barrier reef of Kadidiri. A reef with a top <br>
                at -27 mts, on a sandy bottom of about 70 mts. When the currents are strong <br>
                at certain periods of the tide, the fish life is very dense: Schools of jacks, <br>
                barracudas, white tips, grey reef sharks and sometime hammerheads. Due to <br>
                the strong currents , this dive is only offered to experienced divers.<BR>
                <BR>
                <B>Gunung laut, South Togians, Depth 80 feet</B><BR>
                Coming up from a bottom of more than 200 meters, this pinnacle that culminates <br>
                at -27 meters is visited by numerous pelagic fishes: tunas, white tips and grey reef sharks, schools of jacks. Beautiful coverage of soft corals.<BR>
                This area has several pinnacles that are probably all good diving sites, but we <br>
                practice only two of them. Some room for exploration, mention it on board if <br>
                you are interested.<BR>
                <BR>
                <B>B24 liberator, South Togians, depth 60 feet</B><BR>
                The wreck, a American B24 bomber from the Second World War, lays almost intact <br>
                on a 18 meters bottom. The plane was on its way back to its base on Morotai <br>
                Island (North Mollucas) when an engine went on fire. The pilot managed to <br>
                alight on the waters of this calm bay. All the crew was safe and went to the <br>
                land where the local people rescued them.<BR>
                A large school of jacks is always on the spot; shrimps, glass fishes, crocodile <br>
                fishes. See pictures on the gallery. </p>
