                            <p align="center">The MS Liburan divedestination in 2015 is exclusively the worldclass Halmahera/South Mollucas dive area. Departing harbor and arrival harbor is exclusively Ternate - a island close to Halmahera at South Mollucas.</p>
                            <p align="center"><strong><a href="msliburanboats/liburan_divesite_halmahera.jpg" target="_blank"><img src="msliburanboats/liburan_divesite_halmahera.jpg" alt="liburan liveaboard divedestination halmahera" width="376" height="279" border="0"></a></strong><strong><br>
