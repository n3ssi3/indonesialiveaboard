                            <p align="left">                                  <strong>In general </strong>we will offer 3 boat day dives a day and during one week you   will get 2 night dive plus, each one of them carefully prepared and   checked beforehand, reassuring the right conditions. A typical day on   one of the Ms Liburan Liveaboard offers a simple and relaxing lifestyle   on a spacious and luxurious dive vessel while visiting the most   interesting and beautiful locations, that is Halamhera - North Maluku.   As a general rule, if your hair is dry it's time to dive and if your   hair is wet it's time to eat. </p>
                            <h2>The Morning</h2>
                            <p>The day begins at around 6:30 am ( wake up time ) some of you when   you like short breakfast can make on your own for  breakfast with bread   tea or coffee. We gear up for the first dive of the day about 7:30.<br>
                              After the first dive it's time for real breakfast. Breakfast is a   hot breakfast that you choose from a selection of Chinese and Indonesian   favorites. </p>
                            <h2>The Day </h2>
                            <p>The entire day is dedicated to scuba diving unless we are   steaming to a new dive site or have a shore a expedition planned. There   is often a chance to go ashore if the tender boats are available, just   ask the live aboard crew to take you.         Dives on the live aboard are signaled by a bell, when the bell   rings it's time to begin briefing and later suiting up for the next dive   which will be in 15 minutes.<br>
                              Between dives you can enjoy a massage or just relax on the   sundecks, the shady rear lounge deck or in the air conditioned salon.         Lunch is served around 13:00 after the second dive. It usually   consists of a soup,  and a variety of warm dishes, both chinese and   indonesian.         About 3:00 PM it is time for the third dive of the day, which is   followed by afternoon snack. The food-dive-food routine follows after   the third dive of the day, afterwhich you can enjoy a lazy   late-afternoon on our sundeck, lounging back and soaking in the   wonderful landscape with a cup of coffee or tea and some snacks. </p>
                            <h2>The Evening </h2>
                            <p>A night dive and should not be missed as it offers a chance to   see some of the most unusual reef critters in the world. A night dive   available only 2 times a week.         About 8:00 the bell rings for dinner which is served by the   stewards in the main salon, the dinner time is noramlly after a night   dive. </p>
